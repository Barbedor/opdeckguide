const cloneDeck = (cards) => cards.map((card) => ({ ...card }));

const formatDate = (value) => {
	const [year, month, day] = value.split("-");
	return `${month}/${day}`;
};

const legacyEventTypeMap = {
	Flagship: "Flagship Battle",
	Regional: "Championship",
	"Store Trial": "Standard Battle",
	"Treasure Cup": "Treasure Cup",
};

const normalizeEventType = (eventType) => legacyEventTypeMap[eventType] ?? eventType;

const normalizeEventName = (eventName) =>
	eventName
		.replace(/Flagship/g, "Flagship Battle")
		.replace(/Regional/g, "Championship")
		.replace(/Store Trial/g, "Standard Battle");

const publishedTournamentFormats = new Set(tournamentFormats.map((format) => format.entryFormat));

export const tournamentDecklistEntries = entrySeeds
	.filter((entry) => publishedTournamentFormats.has(entry.format) && entry.region)
	.flatMap((entry, entryIndex) => {
		const leader = leaderIndex[entry.leaderSlug];
		const cards = cloneDeck(deckTemplates[entry.deckTemplate ?? entry.leaderSlug] ?? deckTemplates["yamato-op16"]);
		const matchingViews = tournamentFormats.filter(
			(format) =>
				format.entryFormat === entry.format &&
				format.region === entry.region,
		);

		return matchingViews.map((view) => ({
			...entry,
			leader,
			cards,
			title: normalizeEventName(entry.title),
			eventName: normalizeEventName(entry.eventName),
			eventType: normalizeEventType(entry.eventType),
			region: view.region,
			viewSlug: view.slug,
			viewLabel: view.label,
			entryIndex,
			formattedDate: formatDate(entry.date),
			href: `/tournaments-decklists/${view.slug}/${entry.slug}/`,
		}));
	});

export const getTournamentEntriesByFormat = (viewSlug) =>
	tournamentDecklistEntries
		.filter((entry) => entry.viewSlug === viewSlug)
		.sort((a, b) => b.date.localeCompare(a.date) || a.entryIndex - b.entryIndex);

export const getTournamentEntry = (viewSlug, slug) =>
	tournamentDecklistEntries.find((entry) => entry.viewSlug === viewSlug && entry.slug === slug);

export const getTournamentLeaderStats = (format) => {
	const entries = getTournamentEntriesByFormat(format);
	const total = entries.length;
	const counts = new Map();

	for (const entry of entries) {
		const existing = counts.get(entry.leader.slug) ?? {
			leader: entry.leader,
			count: 0,
			lastWinDate: entry.date,
		};
		existing.count += 1;
		if (entry.date > existing.lastWinDate) existing.lastWinDate = entry.date;
		counts.set(entry.leader.slug, existing);
	}

	return [...counts.values()]
		.map((item) => ({
			...item,
			share: total ? Math.round((item.count / total) * 1000) / 10 : 0,
			lastWinLabel: formatDate(item.lastWinDate),
		}))
		.sort((a, b) => b.count - a.count || a.leader.name.localeCompare(b.leader.name, "en"));
};

export const getTournamentSummary = (format) => {
	const entries = getTournamentEntriesByFormat(format);
	const leaderStats = getTournamentLeaderStats(format);
	const latestEntry = [...entries].sort((a, b) => b.date.localeCompare(a.date))[0];
	const eventTypes = new Set(entries.map((entry) => entry.eventType));

	return {
		totalDecklists: entries.length,
		distinctLeaders: leaderStats.length,
		topLeader: leaderStats[0] ?? null,
		eventTypes: eventTypes.size,
		latestEntry,
	};
};
